function addVariablesForColors({
  addBase,
  theme,
}: {
  addBase: (base: any) => void;
  theme: (path: string) => any;
}) {
  const allColors = flattenColorPalette(theme("colors"));
  const newVars = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ":root": newVars,
  });
}
